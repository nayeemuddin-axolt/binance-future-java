package examples.Indicator;

public class OrderBookEntry {

    private String price;
    private String qty;
  
    public String getPrice() {
      return price;
    }
  
    public void setPrice(String price) {
      this.price = price;
    }
  
    public String getQty() {
      return qty;
    }
  
    public void setQty(String qty) {
      this.qty = qty;
    }    
}
